void main() {
  _test();
}

void _test() {
  test("Orchard tree", () {
    final hashable = OrchardMerkleHashable();
    for (final (index, hash) in hashable.emptyRoots.indexed) {
      expect(BytesUtils.fromHexString(_emptyRoots[index]), hash.toBytes());
    }
    final tree = OrchardShardTree(
      OrchardShardStore(hashable),
      maxCheckpoints: 100,
      depth: 4,
      shardHeight: 3,
    );
    for (final (index, j) in _tVecotor.indexed) {
      final tv = _TestVector.fromJson(j);
      final cmx = OrchardMerkleHash.fromBytes(tv.leaves[index]);
      tree.append(
        value: cmx,
        retention: RetentionCheckpoint(marking: MarkingState.marked, id: index),
      );
      final root = tree.rootAtCheckpointId(index);
      expect(root?.inner.toBytes(), tv.root);
      for (int j = 0; j <= index; j++) {
        final position = LeafPosition(j);
        final witness = tree.witnessAtCheckpointId(
          position: LeafPosition(j),
          checkpointId: index,
        );

        expect(
          witness?.authPath,
          tv.paths[j].map((e) => OrchardMerkleHash.fromBytes(e)).toList(),
        );
        expect(position, witness?.position);
      }
    }
    final s = tree.toSerializeBytes();
    final decode = OrchardShardTree.deserialize(
      bytes: s,
      store: OrchardShardStore(hashable),
      maxCheckpoints: 100,
      depth: 4,
      shardHeight: 3,
    );
    for (final (index, j) in _tVecotor.indexed) {
      final tv = _TestVector.fromJson(j);
      for (int j = 0; j <= index; j++) {
        final position = LeafPosition(j);
        final witness = decode.witnessAtCheckpointId(
          position: LeafPosition(j),
          checkpointId: index,
        );

        expect(
          witness?.authPath,
          tv.paths[j].map((e) => OrchardMerkleHash.fromBytes(e)).toList(),
        );
        expect(position, witness?.position);
      }
    }
    final shards = tree.store.getShards();
    for (final i in shards) {
      final shard = tree.store.getShard(i.rootAddr);
      final dShard = decode.store.getShard(i.rootAddr);
      expect(shard, dShard);
    }
    final checkpoints = tree.store.getCheckpoints();
    for (final i in checkpoints) {
      final checkpoint = decode.store.getCheckpoint(i.key);
      expect(checkpoint?.treeState, i.value.treeState);
    }
  });
}

class _TestVector {
  final List<List<int>> leaves;
  final List<List<List<int>>> paths;
  final List<int> root;
  factory _TestVector.fromJson(Map<String, dynamic> json) {
    return _TestVector(
      leaves:
          json
              .valueAsList<List>("leaves")
              .map((e) => BytesUtils.fromHexString(e))
              .toList(),
      paths:
          json
              .valueEnsureAsList<List>("paths")
              .map((e) => e.map((e) => BytesUtils.fromHexString(e)).toList())
              .toList(),
      root: BytesUtils.fromHexString(json.valueAs("root")),
    );
  }
  Map<String, dynamic> toJson() {
    return {
      "leaves": leaves.map((e) => BytesUtils.toHexString(e)).toList(),
      "paths":
          paths
              .map((e) => e.map((e) => BytesUtils.toHexString(e)).toList())
              .toList(),
      "root": BytesUtils.toHexString(root),
    };
  }

  const _TestVector({
    required this.leaves,
    required this.paths,
    required this.root,
  });
}

